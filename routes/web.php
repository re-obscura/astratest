<?php

use Illuminate\Support\Facades\Route;

/* |-------------------------------------------------------------------------- | SPA catch-all маршрут |-------------------------------------------------------------------------- | | Все запросы (кроме /api/*) обслуживаются одним blade-шаблоном. | Vue Router берёт на себя клиентскую навигацию. | Маршрут с {any?} + where('any', '.*') ловит ВСЁ. | */

Route::get('/{any?}', function () {
    return view('app');
})->where('any', '.*');
